package demotest.basicIntegerPackage;

public class BasicIntegerMethods {
	
	public BasicIntegerMethods(){
		
	}
	
	public int addition(int x, int y){
		
		int sum = 0;
		
		sum = x + y;
	
		return sum;
	}
	
	public int multiply(int x, int y){
		
		int product;
		
		product = x * y;
		
		return product;
		
	}
	
	
}
